package dailyquiz.Feb04;

class Product {
	String name;
	int price;
	int weight;
	
	void setName(String name){
		this.name = name;
	}
	
	String getName() {
		return name;
	}

	void setPrice(int price) {
		this.price = price;
	}
	
	int setPrice() {
		return price;
	}
	
	void setWeight(int weight) {
		this.weight = weight;
	}
	
	int getWeight() {
		return weight;
	}
}
