public class LoginStatus {

	public static void main(String[] args) {
		boolean isLoggedIn = true;
		
		if (isLoggedIn) {
			System.out.println("사용자가 로그인 상태입니다.");
		} else {
			System.out.println("사용자가 로그아웃 상태입니다.");
		}

	}

}
