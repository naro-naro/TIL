package dailyquiz.Jan26;

public class ShoppingCart {

	public static void main(String[] args) {
		int price1 = 5000; // 상품 1의 가격
		int price2 = 8000; // 상품 2의 가격;
		int totalPrice = price1 + price2; // 총 가격
		
		System.out.println("가격 합계 : " + totalPrice + "원입니다.");

	}

}
