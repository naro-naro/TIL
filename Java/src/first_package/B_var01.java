package first;

public class B_var01 {

	public static void main(String[] args) {		
		int a, b, c; // 宣言
		// interger型変数
		a = 0; b = 0; c = 0; // 初期化
		a = 10;
		b = 40;
		a = a + 20;
		c = a + b;
		System.out.println(c);
		
		int level = 10; // 宣言と同時に初期化
		System.out.println(level);
		
		/*
		 * ＜変数名作成の＞
		 * 
		 * アルファベット、数字、_アンダーバー、$ドルだけ使わなければならない
		 * 変数名は数字で始めることはできない
		 * Javaで使用されている予約語は使用できない（while int breakなど）
		 */
	}

}
