package first_package;

public class MethodMath3 {

	public static void main(String[] args) {
		// 삼각함수
		double sin30, cos30, tan30;
		
		// sin 30
		sin30 = Math.sin(Math.toRadians(30));
		System.out.println(sin30);
		
		// cos 30
		cos30 = Math.cos(Math.toRadians(30));
		System.out.println(cos30);
		
		// tan 30
		tan30 = Math.cos(Math.toRadians(30));
		System.out.println(tan30);
	}

}
