package first_package;

public class MethodMath4 {

	public static void main(String[] args) {
		// 거듭 제곱과 제곱근
		double power, root;
		
		// 거듭 제곱
		// 2.0의 3.0제곱
		power = Math.pow(2.0, 3.0);
		System.out.println(power);		
		
		// 제곱근
		// 2.0의 제곱근
		root = Math.sqrt(2.0);
		System.out.println(root);
	}

}
