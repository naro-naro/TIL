package ch04;

public class var01 {

	public static void main(String[] args) {
		int a, b, c;
		a=0; b=0; c=0;
		System.out.println("a="+a);
		System.out.println("b="+b);
		System.out.println("c="+c);
		System.out.println();
		
		a=10;
		b=40;
		c=a+b;
		System.out.println("a="+a);
		System.out.println("b="+b);
		System.out.println("c=a+b="+c);
		System.out.println();
		
		System.out.println(a+"\n"+b+"\n"+c);
		//한줄로 쓰고싶으면 \n을 사용하자

	}

}
