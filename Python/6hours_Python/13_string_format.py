# 방법2 : {} 사용
print("나는 {}살입니다.".format(20))
print("나는 {}색과 {}색을 좋아해요.".format("파란", "분홍"))
print("나는 {0}색과 {1}색을 좋아해요.".format("파란", "분홍"))
print("나는 {1}색과 {0}색을 좋아해요.".format("파란", "분홍"))


#---------------------------------------------------------------------------------------------
print()
#---------------------------------------------------------------------------------------------


# 방법3 : {}와 변수 사용
print("나는 {age}살이고, {color}색을 좋아해요.".format(age = 20, color = "분홍"))
print("나는 {age}살이고, {color}색을 좋아해요.".format(color = "분홍", age = 20))


#---------------------------------------------------------------------------------------------
print()
#---------------------------------------------------------------------------------------------


# 방법4(python v.3.6이상) : {}와 변수 사용(2)
age = 20
color = "보라"
print(f"나는 {age}살이고, {color}색을 좋아해요.")