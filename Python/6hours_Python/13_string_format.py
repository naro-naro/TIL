# 방법1 : %사용
print("나는 20살입니다.")
print("나는 %d살입니다." %20)
# d는 정수를 의미함.

print("나는 %s을 좋아해요." %"파이썬")
# s는 문자열을 의미함.
print("나는 %s살입니다." %20)

print("Apple은 %c로 시작해요." %'A')
# c는 하나의 문자만 가능.

# 문자열 포맷 두 개 %( , )
print("나는 %s색과 %s색을 좋아해요." %('노랑','보라'))


#---------------------------------------------------------------------------------------------
