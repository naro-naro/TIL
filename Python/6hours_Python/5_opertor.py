# 연산자

#사칙연산
print(1+1) # 2
print(3-2) # 1
print(5*2) # 10
print(9/3) # 3

print(2**3) # 2^3 :8
print(5%3) # 나머지연산 :2
print(10%3) # 1
print(5//3) # 몫 : 1
print(10//3) # 3

#부등호
print(3>7) # False
print(6>=2) # True
print(5<=5) # True

#등호
print(3==3) # True
print(4==2) # False
print(3+4==7) # True

print(1!=3) # True
print(not(1!=3)) # False

#and : 모두 참일때만 참
print((3>0) and (3<5)) # True
print((3>0) & (3<5)) # True

#or : 하나만 참이면 참
print((3>0) or (3>5)) # True
print((3>0) | (3>5)) # True

print(2<5<9) # True
print(2<5<3) # False